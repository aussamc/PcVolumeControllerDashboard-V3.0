        public void ClearDisplay()
        {
            Array.Clear(_pixels, 0, _pixels.Length);
        }

        private void SetPixel(int x, int y)
        {
            x += _jitterX;
            y += _jitterY;
            // Clip at the panel edge exactly like the Adafruit GFX library —
            // jittered content is clipped, never wrapped (firmware v2.31).
            if (x < 0 || x >= W || y < 0 || y >= H) return;
            _pixels[y * W + x] = true;
        }

        /// <summary>Horizontal line only (firmware only draws h-lines).</summary>
        public void DrawHLine(int x0, int y, int len)
        {
            for (int i = 0; i < len; i++)
                SetPixel(x0 + i, y);
        }

        /// <summary>Outline rectangle (firmware uses drawRect).</summary>
        public void DrawRect(int x, int y, int w, int h)
        {
            // top & bottom
            for (int i = 0; i < w; i++)
            {
                SetPixel(x + i, y);
                SetPixel(x + i, y + h - 1);
            }
            // left & right
            for (int j = 0; j < h; j++)
            {
                SetPixel(x, y + j);
                SetPixel(x + w - 1, y + j);
            }
        }

        /// <summary>Filled rectangle (firmware uses fillRect).</summary>
        public void FillRect(int x, int y, int w, int h)
        {
            for (int j = 0; j < h; j++)
                for (int i = 0; i < w; i++)
                    SetPixel(x + i, y + j);
        }

        // ── Text rendering ───────────────────────────────────────────────────────

        /// <summary>
        /// Renders one glyph using the Adafruit GFX classic font, exactly as the
        /// firmware's <c>drawChar</c> does: one glyph per BYTE (the ESP32 draws
        /// each raw serial byte; multi-byte UTF-8 chars render as multiple
        /// glyphs), all 8 rows (descenders like g/p/q/y and the comma use bit 7).
        /// </summary>
        private void DrawChar(int x, int y, byte c, int size)
        {
            int idx = c * 5;

            for (int col = 0; col < 5; col++)
            {
                byte colData = Font[idx + col];
                for (int row = 0; row < 8; row++)
                {
                    if ((colData & (1 << row)) != 0)
                    {
                        // Scale the pixel by `size`
                        int px = x + col * size;
                        int py = y + row * size;
                        for (int sy = 0; sy < size; sy++)
                            for (int sx = 0; sx < size; sx++)
                                SetPixel(px + sx, py + sy);
                    }
                }
            }
        }

        /// <summary>
        /// Renders a string left to right, one glyph per UTF-8 byte (matching what
        /// the firmware receives over serial); each glyph occupies 6*size pixels.
        /// </summary>
        private void DrawString(string text, int x, int y, int size)
        {
            if (string.IsNullOrEmpty(text)) return;
            DrawBytes(Encoding.UTF8.GetBytes(text), x, y, size);
        }

        private void DrawBytes(ReadOnlySpan<byte> bytes, int x, int y, int size)
        {
            int cursorX = x;
            foreach (byte c in bytes)
            {
                DrawChar(cursorX, y, c, size);
                cursorX += 6 * size;
            }
        }

        /// <summary>
        /// Width in pixels, counted per UTF-8 byte — the firmware's getTextBounds
        /// counts each raw byte as one 6*size-wide glyph cell.
        /// </summary>
        private int TextWidth(string text, int size) =>
            (text is null ? 0 : Encoding.UTF8.GetByteCount(text)) * 6 * size;

        /// <summary>Centers text horizontally then calls DrawString.</summary>
        private void DrawCenteredSmall(string text, int y, int size)
        {
            if (text == null) text = string.Empty;
            int tw = TextWidth(text, size);
            int x = Math.Max(0, (W - tw) / 2);
            DrawString(text, x, y, size);
        }

        // ── Firmware display modes ────────────────────────────────────────────────

        /// <summary>Mode: App name + volume (default/else branch in firmware).</summary>
        public void RenderAppVolume(string label, int volume, bool muted, string status)
        {
            ClearDisplay();
            DrawCenteredSmall(label, 0, 1);
            DrawHLine(0, 12, 126);
            string volumeText = $"{volume}%";
            DrawString(volumeText, (128 - TextWidth(volumeText, 2)) / 2, 22, 2);
            DrawCenteredSmall(muted ? "Muted" : "Unmuted", 46, 1);
            // Bottom line at y54 (not y56) so it ends at row 60 — see AntiBurnJitterMax.
            DrawCenteredSmall(status, 54, 1);
        }

        /// <summary>
        /// Mode: Large volume number. Mirrors the firmware LARGE_VOLUME branch
        /// (v2.28): channel name at size 2 (drawn at y3), a rule at y20, then a
        /// size-4 value at y26. There is no separate mute strip — when muted, the
        /// big number is replaced by the word "MUTE"; when unmuted, the volume
        /// number returns. The header sits at y3 (not y0) so all content is within
        /// rows 3..60, leaving a top margin clear of the anti-burn jitter. The
        /// header is truncated to 10 chars (12 px/char at size 2) so it can't run
        /// past the 128 px panel — labels arrive up to 18 chars (firmware v2.31).
        /// </summary>
        public void RenderLargeVolume(string label, int volume, bool muted)
        {
            ClearDisplay();
            // Truncate at 10 BYTES, matching the firmware's Arduino
            // String.substring(0, 10) which operates on raw (UTF-8) bytes — the
            // slice is drawn as bytes so a split multi-byte char renders the same
            // stray glyph the device would show, not a '�' substitution.
            ReadOnlySpan<byte> headerBytes = Encoding.UTF8.GetBytes(label ?? string.Empty);
            if (headerBytes.Length > 10) headerBytes = headerBytes[..10];
            int headerX = Math.Max(0, (W - headerBytes.Length * 6 * 2) / 2);
            DrawBytes(headerBytes, headerX, 3, 2);
            DrawHLine(0, 20, 126);
            string bigText = muted ? "MUTE" : $"{volume}%";
            DrawString(bigText, (128 - TextWidth(bigText, 4)) / 2, 26, 4);
        }

        /// <summary>Mode: Mute status.</summary>
        public void RenderMuteStatus(string label, int volume, bool muted)
        {
            ClearDisplay();
            DrawCenteredSmall(muted ? "MUTED" : "ACTIVE", 12, 2);
            DrawCenteredSmall(label, 40, 1);
            DrawCenteredSmall($"{volume}%", 54, 1);
        }

        /// <summary>Mode: App/device name (channel scroll).</summary>
        public void RenderAppOrDeviceName(int channelNum, string label, string status, int volume)
        {
            ClearDisplay();
            DrawCenteredSmall($"CHANNEL {channelNum}", 0, 1);
            DrawHLine(0, 12, 126);
            DrawCenteredSmall(label, 24, 1);
            DrawCenteredSmall(status, 40, 1);
            DrawCenteredSmall($"{volume}%", 54, 1);
        }

        /// <summary>Mode: Bar/percentage view.</summary>
        public void RenderBarPercent(string label, int volume, bool muted)
        {
            ClearDisplay();
            DrawCenteredSmall(label, 0, 1);
            DrawHLine(0, 12, 126);
            DrawRect(8, 28, 112, 14);
            // Integer truncation, matching the firmware's Arduino map():
            // map(v, 0, 100, 0, 108) = v * 108 / 100 with integer division
            // (Math.Round would land 1px wide at e.g. v=99: 107 vs the device's 106).
            int barWidth = Math.Clamp(volume, 0, 100) * 108 / 100;
            if (barWidth > 0) FillRect(10, 30, barWidth, 10);
            // Both bottom lines lifted (y46/y54, not y48/y56) so the last row ends at
            // 60, clear of the anti-burn jitter — see AntiBurnJitterMax.
            DrawCenteredSmall($"{volume}%", 46, 1);
            DrawCenteredSmall(muted ? "Muted" : "Unmuted", 54, 1);
        }

        // ── Anti-burn-in ─────────────────────────────────────────────────────────

        /// <summary>
        /// Maximum jitter offset per axis (firmware v2.31): the drawing origin
        /// walks a 3×3 grid of (0..2, 0..2) pixel offsets. Every render mode keeps
        /// its base content within x0..(Width-1-AntiBurnJitterMax) = x0..125 and
        /// y0..(Height-1-AntiBurnJitterMax) = y0..61 so a full jitter never clips
        /// a lit pixel. (Replaces the pre-v2.31 hardware SETDISPLAYOFFSET wrap and
        /// its AntiBurnMaxOffset bottom-margin rule.)
        /// </summary>
        public const int AntiBurnJitterMax = 2;

        /// <summary>Number of positions in the firmware's jitter walk (3×3 grid).</summary>
        public const int AntiBurnJitterSteps = 9;

        /// <summary>
        /// Interval between jitter steps in milliseconds — mirrors the firmware's
        /// <c>BURN_JITTER_PERIOD_MS</c> (one step every 30 s; full 9-position cycle
        /// every 4.5 min).
        /// </summary>
        public const long AntiBurnJitterPeriodMs = 30000;

        // The firmware's walk order (BURN_JITTER_X/Y): perimeter clockwise, then
        // centre; centre→origin is a diagonal neighbour so the loop closes
        // without a jump.
        private static readonly (int Dx, int Dy)[] JitterWalk =
        {
            (0, 0), (1, 0), (2, 0), (2, 1), (2, 2), (1, 2), (0, 2), (0, 1), (1, 1),
        };

        /// <summary>
        /// The jitter offset for a given step counter (typically wall-clock ms /
        /// <see cref="AntiBurnJitterPeriodMs"/>), mirroring the firmware's
        /// <c>updateAntiBurnInJitter</c> table lookup.
        /// </summary>
        public static (int Dx, int Dy) AntiBurnJitterForStep(long step)
        {
            long i = step % AntiBurnJitterSteps;
            if (i < 0) i += AntiBurnJitterSteps;
            return JitterWalk[i];
        }

        /// <summary>
        /// Sets the anti-burn-in jitter offset applied to everything drawn after
        /// the call (offsets each pixel, exactly as the firmware offsets each draw
        /// call). Call before a Render* method so the preview matches the device;
        /// content shifted past the panel edge is clipped, never wrapped.
        /// </summary>
        public void SetAntiBurnJitter(int dx, int dy)
        {
            _jitterX = dx;
            _jitterY = dy;
        }

    }
